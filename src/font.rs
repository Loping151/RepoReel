pub type Rgb = (u8, u8, u8);

const GLYPH_W: usize = 5;
const GLYPH_H: usize = 7;

pub fn render_char(ch: char, fg: Rgb, bg: Rgb, out: &mut [Rgb], cell_w: usize, cell_h: usize) {
    debug_assert_eq!(out.len(), cell_w.saturating_mul(cell_h));
    out.fill(bg);

    if ch == ' ' || cell_w == 0 || cell_h == 0 {
        return;
    }

    if render_symbol(ch, fg, out, cell_w, cell_h) {
        return;
    }

    let glyph = glyph_bits(ch).unwrap_or_else(|| glyph_bits('?').expect("fallback glyph exists"));
    let scale = (cell_w / GLYPH_W).min(cell_h / GLYPH_H).max(1);
    let ink_w = GLYPH_W * scale;
    let ink_h = GLYPH_H * scale;
    let offset_x = cell_w.saturating_sub(ink_w) / 2;
    let offset_y = cell_h.saturating_sub(ink_h) / 2;

    for (row, bits) in glyph.iter().copied().enumerate() {
        for col in 0..GLYPH_W {
            if bits & (1 << (GLYPH_W - 1 - col)) == 0 {
                continue;
            }
            fill_rect(
                out,
                cell_w,
                offset_x + col * scale,
                offset_y + row * scale,
                scale,
                scale,
                fg,
            );
        }
    }
}

fn render_symbol(ch: char, fg: Rgb, out: &mut [Rgb], cell_w: usize, cell_h: usize) -> bool {
    match ch {
        '█' => {
            out.fill(fg);
            true
        }
        '■' => {
            let margin_x = (cell_w / 5).max(1);
            let margin_y = (cell_h / 5).max(1);
            fill_rect(
                out,
                cell_w,
                margin_x,
                margin_y,
                cell_w.saturating_sub(margin_x * 2),
                cell_h.saturating_sub(margin_y * 2),
                fg,
            );
            true
        }
        '●' => {
            let radius_x = (cell_w / 2).saturating_sub(1).max(1);
            let radius_y = (cell_h / 2).saturating_sub(2).max(1);
            let center_x = cell_w / 2;
            let center_y = cell_h / 2;
            for y in 0..cell_h {
                for x in 0..cell_w {
                    let dx = x.abs_diff(center_x) * radius_y;
                    let dy = y.abs_diff(center_y) * radius_x;
                    if dx * dx + dy * dy <= radius_x * radius_x * radius_y * radius_y {
                        out[y * cell_w + x] = fg;
                    }
                }
            }
            true
        }
        '▶' => {
            let left = cell_w / 4;
            let right = cell_w.saturating_sub(left).saturating_sub(1);
            let center_y = cell_h / 2;
            let half_h = (cell_h / 3).max(1);
            for y in center_y.saturating_sub(half_h)..=(center_y + half_h).min(cell_h - 1) {
                let distance = y.abs_diff(center_y);
                let row_left = left + distance * right.saturating_sub(left) / half_h.max(1);
                for x in row_left..=right {
                    out[y * cell_w + x] = fg;
                }
            }
            true
        }
        '░' | '▒' => {
            let modulus = if ch == '░' { 4 } else { 3 };
            for y in 0..cell_h {
                for x in 0..cell_w {
                    if (x + y) % modulus == 0 || (ch == '▒' && (x + y) % modulus == 1) {
                        out[y * cell_w + x] = fg;
                    }
                }
            }
            true
        }
        '·' => {
            let dot = (cell_w.min(cell_h) / 5).max(1);
            fill_rect(
                out,
                cell_w,
                cell_w.saturating_sub(dot) / 2,
                cell_h.saturating_sub(dot) / 2,
                dot,
                dot,
                fg,
            );
            true
        }
        '─' | '│' | '┌' | '┐' | '└' | '┘' | '├' | '┤' | '┬' | '┴' | '┼' => {
            draw_box_line(ch, fg, out, cell_w, cell_h);
            true
        }
        '╲' | '╱' => {
            draw_diagonal(ch, fg, out, cell_w, cell_h);
            true
        }
        _ => false,
    }
}

fn draw_box_line(ch: char, fg: Rgb, out: &mut [Rgb], cell_w: usize, cell_h: usize) {
    let cx = cell_w / 2;
    let cy = cell_h / 2;
    let thickness = (cell_w.min(cell_h) / 6).max(1);
    let left = matches!(ch, '─' | '┐' | '┘' | '┤' | '┬' | '┴' | '┼');
    let right = matches!(ch, '─' | '┌' | '└' | '├' | '┬' | '┴' | '┼');
    let up = matches!(ch, '│' | '└' | '┘' | '├' | '┤' | '┴' | '┼');
    let down = matches!(ch, '│' | '┌' | '┐' | '├' | '┤' | '┬' | '┼');

    if left {
        fill_rect(out, cell_w, 0, cy, cx + 1, thickness, fg);
    }
    if right {
        fill_rect(
            out,
            cell_w,
            cx,
            cy,
            cell_w.saturating_sub(cx),
            thickness,
            fg,
        );
    }
    if up {
        fill_rect(out, cell_w, cx, 0, thickness, cy + 1, fg);
    }
    if down {
        fill_rect(
            out,
            cell_w,
            cx,
            cy,
            thickness,
            cell_h.saturating_sub(cy),
            fg,
        );
    }
}

fn draw_diagonal(ch: char, fg: Rgb, out: &mut [Rgb], cell_w: usize, cell_h: usize) {
    let thickness = (cell_w.min(cell_h) / 8).max(1);
    for y in 0..cell_h {
        let x = if ch == '╲' {
            y * cell_w / cell_h.max(1)
        } else {
            cell_w.saturating_sub(1 + y * cell_w / cell_h.max(1))
        };
        fill_rect(
            out,
            cell_w,
            x.saturating_sub(thickness / 2),
            y,
            thickness,
            1,
            fg,
        );
    }
}

fn fill_rect(
    out: &mut [Rgb],
    stride: usize,
    x: usize,
    y: usize,
    width: usize,
    height: usize,
    color: Rgb,
) {
    if stride == 0 || width == 0 || height == 0 {
        return;
    }
    let rows = out.len() / stride;
    let end_y = y.saturating_add(height).min(rows);
    let end_x = x.saturating_add(width).min(stride);
    for row in y..end_y {
        let start = row * stride + x.min(stride);
        let end = row * stride + end_x;
        out[start..end].fill(color);
    }
}

