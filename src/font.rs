#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn ascii_visible_range_has_glyphs() {
        for byte in 0x20_u8..=0x7e {
            assert!(glyph_bits(char::from(byte)).is_some());
        }
    }

    #[test]
    fn rendered_letter_contains_foreground_and_background() {
        let fg = (255, 255, 255);
        let bg = (0, 0, 0);
        let mut out = vec![bg; 12 * 20];

        render_char('R', fg, bg, &mut out, 12, 20);

        assert!(out.contains(&fg));
        assert!(out.contains(&bg));
    }

    #[test]
    fn box_drawing_characters_render_ink() {
        for ch in ['─', '│', '┌', '┐', '└', '┘', '├', '┤', '┬', '┴', '┼'] {
            let fg = (255, 255, 255);
            let bg = (0, 0, 0);
            let mut out = vec![bg; 12 * 20];

            render_char(ch, fg, bg, &mut out, 12, 20);

            assert!(out.contains(&fg), "{ch}");
        }
    }
}
